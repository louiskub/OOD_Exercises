# อธิบาย Input : จะมีแค่รูปแบบเดียวคือ   < a  b >  โดยที่  a = น้ำหนักของจาน  ,  b = ความถี่ของจาน




lst = [[int(k) for k in e.split()] for e in input('Enter Input : ').split(',')]

stack = []
# print(stack.pop())
for i in range(len(lst)) :
    if not stack or stack[-1][0] >= lst[i][0]:
        stack.append(lst[i])
    else :
        while stack and stack[-1][0] < lst[i][0]:
            print(stack.pop()[1])
        stack.append(lst[i])